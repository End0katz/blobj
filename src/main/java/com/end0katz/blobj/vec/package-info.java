/**
 * A class containing some vectors:
 *
 *
 * {@code Vec}s are the interfaces for anything wanting to be a vector
 *
 * {@code Int}s are vectors that implements the {@code Vec} interfaces for
 * {@link Integer}s
 *
 *
 * {@code Float}s are vectors that implement the {@code Vec} interface for
 * {@link Double}s
 *
 *
 * {@code Obj<T>}s are vectors that implement the {@code Vec} interface for
 * arbitrary {@link Object}s
 */
package com.end0katz.blobj.vec;
